CREATE TABLE ORDERS(
  ID INT(20) NOT NULL AUTO_INCREMENT,
  ROOMID INT(20) NOT NULL COMMENT '房间ID',
  GUESTNAME VARCHAR(20) NOT NULL COMMENT '住客名',
  MPHONE VARCHAR(13) NOT NULL COMMENT '移动电话',
  CHANNEL VARCHAR(10) NOT NULL COMMENT '来源',
  NIGHTS INT(2) NOT NULL COMMENT '总天数',
  TOTAL_PRICE FLOAT NOT NULL COMMENT '总价',
  REMARK VARCHAR(40) NOT NULL COMMENT '备注',
  PAYSTATUS TINYINT(1) DEFAULT '0' COMMENT '付款状态',
  NEEDCAR TINYINT(1) DEFAULT '0' COMMENT '需要接车',
  CHECKIN DATE NOT NULL ,
  CHECKOUT DATE NOT NULL,
  STATUS TINYINT(1),
  CREATED DATE NOT NULL,
  CREATEDBY VARCHAR(20) NOT NULL,
  UPDATED DATE NOT NULL,
  UPDATEDBY VARCHAR(20) NOT NULL,
  PRIMARY KEY(ID)
)ENGINE=InnoDB DEFAULT CHARSET=utf8 AUTO_INCREMENT=1;

CREATE TABLE ORDER_ROOMS(
